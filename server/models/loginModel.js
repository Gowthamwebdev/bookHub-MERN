import mongoose from "mongoose";

// Connect to MongoDB

const LoginSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const LoginModel = mongoose.model("loginauth", LoginSchema);
